#include "libft.h"

void	ft_putnbr(int n)
{
	long int a;

	a = n;
	if (a < 0)
	{
		a *= -1;
		ft_putchar('-');
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
	}
	ft_putchar(a % 10 + '0');
}
