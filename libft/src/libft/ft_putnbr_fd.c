#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int a;

	a = n;
	if (a < 0)
	{
		a *= -1;
		ft_putchar_fd('-', fd);
	}
	if (a > 9)
	{
		ft_putnbr_fd(a / 10, fd);
	}
	ft_putchar_fd(a % 10 + '0', fd);
}
