#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t d;

	d = 0;
	if (s == NULL || f == NULL)
		return ;
	while (*s)
	{
		f(d++, s++);
	}
}
